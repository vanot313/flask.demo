P = 0  # 待评估数据资产的价值，最终结果
n = 5  # 待评估数据资产的预期获利持续年限
r = 0.1  # 待评估数据资产的折现率,需要依据实际情况变化
R = []  # 第i年的预期收益

# 为R赋值
for i in range(1, n + 1):
    print("请输入第", i, "年的预期收益: ")
    RI = input()
    R.append(float(RI))

# 计算收益法下的数据资产评估价值
for i in range(1, n+1):
    P = P + R[i-1] / (1 + r)**i

print("用收益法计算获得的数据资产评估价值为", P)
